package loop_working;

public class Do_While_Loop {

    public static void main (String[] args){

        //SYNTAX do {code}while condition;
//increment
       int i=10;
       do {
            System.out.println(" this is do while code..."+i); i++;}
      while (i<=30);
//decrement
         i=55;
        do {
             System.out.println("this is do while loop in decrement.."+i); i--;}
        while (i>=25);



    }
}
